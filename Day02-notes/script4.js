console.log("===== while loop =============");
//while loop
var i = 15;
while (i < maxNumber) {
  i = i + 1;
  console.log(i);
}

console.log("===== do while loop =============");
//do while loop
i = 15;
do {
  console.log(i);
  i = i + 1;
} while (i < maxNumber);

console.log("===== for loop =============");
//for loop
for (i = 0; i < maxNumber; i++) {
  console.log(i);
}
