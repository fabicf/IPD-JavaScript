// switch statements

var option = 2;

switch (option) {
  case 1:
    console.log("I am 1");
    break;
  case 2:
    console.log("I am 2");
    break;
  case 3:
    console.log("I am 3");
    break;
  default:
    console.log("I am default");
}

// switch with string

var option = "b";

switch (option) {
  case "a":
    console.log("I am A");
    break;
  case "b":
    console.log("I am B");
    break;
  case "c":
    console.log("I am C");
    break;
  default:
    console.log("I am default");
}
